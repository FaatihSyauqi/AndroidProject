public class Belanja {
    private int foto;
    private String nama_belanja;
    private String alamat_belanja;
    private String jam_belanja;
    private String jamakhir_belanja;
    private String deskripsi_belanja;
    private String fasilitas_belanja;
    private String telepon_belanja;
    private String website_belanja;
    private String lat_belanja;
    private String long_belanja;

    public Belanja() {
    }

    public int getFoto() {
        return foto;
    }

    public String getNama_belanja() {
        return nama_belanja;
    }

    public String getAlamat_belanja() {
        return alamat_belanja;
    }

    public String getJam_belanja() {
        return jam_belanja;
    }

    public String getJamakhir_belanja() {
        return jamakhir_belanja;
    }

    public String getDeskripsi_belanja() {
        return deskripsi_belanja;
    }

    public String getFasilitas_belanja() {
        return fasilitas_belanja;
    }

    public String getTelepon_belanja() {
        return telepon_belanja;
    }

    public String getWebsite_belanja() {
        return website_belanja;
    }

    public String getLat_belanja() {
        return lat_belanja;
    }

    public String getLong_belanja() {
        return long_belanja;
    }
}
